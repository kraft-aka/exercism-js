function timeToMixJuice(name) {
  switch (name) {
    case "Pure Strawberry Joy":
      return 0.5;
      break;
    case "Energizer":
      return 1.5;
      break;

    case "Green Garden":
      return 1.5;

    case "Tropical Island":
      return 3;
      break;

    case "All or Nothing":
      return 5;
      break;
    default:
      return 2.5;
  }
}

